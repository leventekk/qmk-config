void oled_render_master_state(void) {
    switch (get_highest_layer(layer_state)) {
        case BASE:
            oled_write_raw_P(base_layer_text, sizeof(base_layer_text));
            break;
        case LOWER:
            oled_write_raw_P(lower_layer_text, sizeof(lower_layer_text));
            break;
        case RAISE:
            oled_write_raw_P(raise_layer_text, sizeof(raise_layer_text));
            break;
        case ADJUST:
            oled_write_raw_P(adjust_layer_text, sizeof(adjust_layer_text));
            break;
        case NUMERIC:
            oled_write_raw_P(numeric_layer_text, sizeof(numeric_layer_text));
            break;
    }

    led_t led_state = host_keyboard_led_state();

    if (led_state.caps_lock) {
        oled_write_raw_P(caps_lock_indicator, sizeof(caps_lock_indicator));
    }
}
